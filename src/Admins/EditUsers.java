    private void loadUserDetails() {
    if (this.userId == null || this.userId.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Error: User ID is missing.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    config dbc = new config();
    try (Connection conn = dbc.getConnection();
         PreparedStatement ps = conn.prepareStatement("SELECT * FROM users WHERE id = ?")) {
        
        ps.setString(1, this.userId);
        ResultSet rs = ps.executeQuery();
        
        if (rs.next()) {
            fname.setText(rs.getString("fname"));
            lname.setText(rs.getString("lname"));
            email.setText(rs.getString("email"));
            uname.setText(rs.getString("uname"));
            utype.setSelectedItem(rs.getString("account_type"));
            status.setSelectedItem(rs.getString("status"));
            Phone.setText(rs.getString("contact"));
        }
        
    } catch (SQLException e) {
        e.printStackTrace();
    }
}

