    public boolean duplicateChecker(String userId) {
    config conf = new config();
    try {
        // Query to check if the username or email exists but ignore the current user ID
        String query = "SELECT * FROM users WHERE (uname = ? OR email = ?) AND id != ?";
        PreparedStatement ps = conf.getConnection().prepareStatement(query);
        ps.setString(1, uname.getText());  // Username
        ps.setString(2, email.getText());  // Email
        ps.setString(3, userId);           // Current User ID (excluded)

        ResultSet resultSet = ps.executeQuery();
        
        if (resultSet.next()) {
            String existingEmail = resultSet.getString("email");
            String existingUsername = resultSet.getString("uname");

            if (existingEmail.equals(email.getText())) {
                JOptionPane.showMessageDialog(null, "Email is already in use!");
                email.setText(null);
            }
            if (existingUsername.equals(uname.getText())) {
                JOptionPane.showMessageDialog(null, "Username is already taken!");
                uname.setText(null);
            }
            return true; // Duplicate found
        }
        return false; // No duplicates
    } catch (SQLException ex) {
        ex.printStackTrace();
        return false;
    }
}

